	<h2>Biography</h2>

	Paul Tucker is a composer/pianist currently teaching, performing and writing in West London.
	He studied at the Royal Welsh College of Music and Drama and the London College of music and 
	Media. 
	<p>
	He graduated in 2001 with a Masters degree in composition for Film and Television, 
	His Acclaimed compositions include works for solo piano, chamber ensembles, Choral music, 
	theatre music and full orchestra. 
	<p>
	He has won many piano competitions, and also the ABAC Premio 
	Franz Liszt International Competition for composers, held in Grottamare, Italy.
	</p>
	
